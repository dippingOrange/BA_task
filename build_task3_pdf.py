    pdf.body('胜率与盈亏比的关系是交易策略的核心权衡：一个胜率 90% 的策略（每次赚 1%，亏损时亏 9%）比一个胜率 40% 的策略（每次赚 10%，亏损时亏 2%）最终的期望收益可能更低。这就是为什么趋势跟踪策略（典型胜率 30-40%）可以长期盈利——它们靠的是"让利润奔跑、果断止损"的不对称盈亏比。')
    pdf.body('交易次数则是衡量策略活跃度的指标。过多的交易会因手续费（本回测按万分之三计算）和滑点侵蚀利润；过少的交易则说明策略过于保守，可能错过了大量交易机会。在 242 个交易日的回测期内，5/15 策略产生了 18 笔交易（9 组买入-卖出对），即平均约每 27 个交易日完成一个交易回合，交易频率较为适中。')

