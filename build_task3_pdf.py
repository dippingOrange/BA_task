    pdf.sub('2.2 年化收益率（Annualized Return）')
    pdf.body('年化收益率将不同期限的回报标准化到"年"为单位，使得不同时间跨度的策略可以直接比较。其计算公式基于复利思想：')
    pdf.formula_box([
        '年化收益率 = (V_final / V_initial)^(252 / n) - 1',
        '',
        '其中:',
        '  V_final = 期末资金',
        '  V_initial = 期初资金',
        '  252 = 一年中的交易日数量（A股标准）',
        '  n = 回测期的实际交易日数',
    ])
    pdf.body('为什么需要年化？假设策略A在半年（126天）内盈利10%，年化收益率为 (1.1)^(252/126)-1 ≈ 21%。策略B在两年（504天）内盈利25%，年化收益率为 (1.25)^(252/504)-1 ≈ 11.8%。仅看累计回报，策略B（25%）似乎优于策略A（10%），但年化指标告诉我们策略A的年度化效率明显更高。')
    pdf.body('年化收益率的隐含假设是"复利再投资"——即期间的盈利可以继续投入并产生同等回报水平。在实际量化交易中，这一假设并非总是成立（例如策略容量限制不允许无限制放大头寸），但作为比较不同策略的标准化工具有其不可替代的价值。')

    # 2.3
    pdf.sub('2.3 最大回撤（Maximum Drawdown, MDD）')
    pdf.body('最大回撤是衡量策略风险的核心指标，它描述了在回测期内，资金曲线从任意一个历史峰值到其后续最低谷值的最大跌幅。简言之，它回答了一个交易者最关心的问题："在最糟糕的情况下，我会亏掉多少钱？"')
    pdf.body('MDD 的计算需要逐步回溯资金曲线：对于每一天 t，计算该日资金 V_t 相对于在此之前所有天中出现的最高资金水平（running peak）的跌幅，然后在所有交易日中取这些跌幅的最大值。')
    pdf.formula_box([
        'MDD 计算步骤:',
        '  第1步: 计算累积最高资金: Peak(t) = max(V_1, V_2, ..., V_t)',
        '  第2步: 计算每日回撤: DD(t) = (V_t - Peak(t)) / Peak(t)',
        '  第3步: 取最小值: MDD = min(DD(1), DD(2), ..., DD(n))',
        '',
        'MDD 含义: 策略在回测期内资金曲线从顶到底的最大百分比跌幅。',
        '  例: 资金从 120万 峰值跌至 96万, MDD = (96-120)/120 = -20%。',
    ])
    pdf.body('MDD 的重要性在于它直接关联交易者的心理承受能力和资金管理。一个 MDD 为 -30% 的策略，即使最终盈利可观，也可能在中途令交易者因无法承受亏损而被迫终止——这就是"策略回撤与实际执行之间的心理鸿沟"。')
    pdf.body('不同类型策略的典型 MDD 范围：趋势跟踪类策略的 MDD 通常在 -15% 到 -30% 之间（因趋势反转期的连续亏损）；高频/统计套利类策略的 MDD 通常低于 -5%；而长期价值投资策略的 MDD 可能高达 -40% 甚至更多，但伴随更高的长期回报。本报告中 5/15 双均线策略的 MDD 为 -14.56%，处于趋势跟踪策略的较优水平。')

    # 2.4
    pdf.sub('2.4 夏普比率（Sharpe Ratio）')
    pdf.body('夏普比率由诺贝尔经济学奖得主 William F. Sharpe 于 1966 年提出，是衡量"每单位风险所获得的超额回报"的最经典指标。它同时考虑了收益和风险两个维度，使得不同风险水平的策略可以公平比较。')
    pdf.formula_box([
        '夏普比率公式:',
        '  Sharpe = (R_p - R_f) / σ_p',
        '',
        '其中:',
        '  R_p = 策略年化收益率',
        '  R_f = 无风险利率（本报告取 3%，约等于一年期国债收益率）',
        '  σ_p = 策略日收益率的年化标准差（波动率）',
        '',
        '年化转换: 日收益率标准差 × sqrt(252) → 年化波动率',
        '        日收益率均值 × 252 → 年化收益率',
    ])
    pdf.body('夏普比率的解读标准（学术与业界共识）：')
    pdf.bullet('夏普比率 < 0：策略表现差于无风险收益（存银行都比这强），应果断放弃。', '负值：')
    pdf.bullet('夏普比率 0 ~ 1：策略有正的超额收益但风险调整后效率较低，勉强可接受。', '0 - 1：')
    pdf.bullet('夏普比率 1 ~ 2：策略表现良好，风险控制较优，处于业界一般水平以上。', '1 - 2：')
    pdf.bullet('夏普比率 > 2：策略极为优秀，风险调整后收益非常出众，属于顶级量化基金水准。', '> 2：')
    pdf.body('需要注意的是，夏普比率有其局限性：它假设收益率服从正态分布，而真实的金融收益率往往存在"肥尾"特征（极端事件出现概率远高于正态分布预测）。此外，夏普比率将"上行波动"（价格上涨）和"下行波动"（价格下跌）同等对待，这与投资者的真实偏好不一致——投资者通常欢迎上行波动而厌恶下行波动。因此，Sortino 比率（仅用下行标准差计算）和 Calmar 比率（收益/最大回撤）等改进指标也在实践中广泛使用。')
    pdf.body('本报告中 5/15 策略的夏普比率为 -1.15，低于 0 的原因在于策略在整个回测期内整体亏损（累计回报 -9.51%），年化收益率甚至低于 3% 的无风险利率。这并非策略本身的问题，而是因为贵州茅台在回测期整体下跌了约 16%，在如此熊市环境下大多数做多策略都难以取得正的夏普比率。')

