    pdf.body('累计回报是最直观的绩效指标，表示策略在整个回测期内的总收益百分比。计算公式为：Cumulative Return = (V_final / V_initial) - 1，其中 V_final 是期末资金，V_initial 是期初资金。例如，初始资金 100 万，期末资金变成 110 万，累计回报即为 (110万 / 100万) - 1 = 10%。')
    pdf.body('累计回报的优势在于简洁明了——任何一个投资者都能立刻理解"赚了百分之几"。其核心局限在于：它不考虑时间跨度。一个 5 年实现 50% 回报的策略和一个 1 个月实现 50% 回报的策略，在累计回报这个指标上完全相同，但显然两者的风险收益特征天差地别。因此，累计回报必须搭配年化收益率和最大回撤等指标一起使用才有完整意义。')

